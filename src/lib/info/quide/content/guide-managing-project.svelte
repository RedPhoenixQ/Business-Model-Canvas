<p>
  A new project can be created by choosing from one of the templates in the
  <i>New</i> section of the file menu.
</p>
<p>
  To name a project you can type a name in the text filed at the top left of the
  screen.
</p>
<p>
  Projects are automatically saved in the browser so that no work gets lost. To
  save it to you computer for storing or sharing with others you can download
  the project file by pressing <i>Download</i> in the file menu.
</p>
<p>
  To open a project from a file you can press the <i>Open file</i> option or just
  drag the file to the window and drop it. You will be asked to confirm if you want
  to open the file as that would remove the current project unless its downloaded
  beforehand.
</p>

<p>
  Physical assets – equipment for development, soft- and hardware, vehicles,
  etc.
</p>

<p>
  Intellectual assets – copyrights, brands, patents, partnerships, corporate
  secrets, client database, etc.
</p>

<p>Soft assets – know-how, creativity, employee competence, etc.</p>

<p>
  Financial assets – liquid assets, credits, option programs for employees, etc.
</p>


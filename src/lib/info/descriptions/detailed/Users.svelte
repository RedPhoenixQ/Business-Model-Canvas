<p>
  Mass market – no difference between customer segments, offering and
  distribution channel. Customer relations focus on large group with overall
  same need and problem.
</p>

<p>
  Niche – adoption of a specific customer segment. Offering, distribution
  channels, and customer relations are all adjusted to these specific relations
  in its niche market.
</p>

<p>
  Segmented – specified towards a customer that has the same general need and
  problem, but with minor differences.
</p>

<p>
  Diversified – Offering to customer is different but present because of
  existing demand, availability, or the solution/satisfaction can be reach by
  different offerings.
</p>

<p>
  Multifaceted platform – Offering that can be offered to two or more customer
  segments.
</p>

<p>
  Cost-driven business model:<br />
  Focus on cost reduction/minimizing through for example maximizing automation and
  high degree of outsourcing
</p>

<p>
  Value-driven business model:<br />
  Focus on added value to the offering through activities and resources.
</p>

<div>
  Examples:
  <ul class="list-disc pl-6">
    <li>Fixed costs</li>
    <li>Variable costs</li>
    <li>Scalability</li>
    <li>Economies of scope</li>
  </ul>
</div>
